/**
 * Get health milestones by category
 */
export const getHealthMilestonesByCategory = (
  category: HealthMilestone['category']
): HealthMilestone[] => {
  return healthTimeline.filter((milestone) => milestone.category === category);
};

/**
 * Get the next health milestone based on quit time
 */
export const getNextHealthMilestone = (
  minutesSinceQuit: number
): HealthMilestone | null => {
  const nextMilestone = healthTimeline.find(
    (milestone) => milestone.timeInMinutes > minutesSinceQuit
  );
  return nextMilestone || null;
};

/**
 * Get all achieved health milestones
 */
export const getAchievedHealthMilestones = (
  minutesSinceQuit: number
): HealthMilestone[] => {
  return healthTimeline.filter(
    (milestone) => milestone.timeInMinutes <= minutesSinceQuit
  );
};

/**
 * Get progress to next milestone (0-100)
 */
export const getProgressToNextMilestone = (
  minutesSinceQuit: number
): { progress: number; nextMilestone: HealthMilestone | null } => {
  const achieved = getAchievedHealthMilestones(minutesSinceQuit);
  const nextMilestone = getNextHealthMilestone(minutesSinceQuit);

  if (!nextMilestone) {
    return { progress: 100, nextMilestone: null };
  }

  const lastAchieved = achieved[achieved.length - 1];
  const startTime = lastAchieved ? lastAchieved.timeInMinutes : 0;
  const endTime = nextMilestone.timeInMinutes;
  const currentTime = minutesSinceQuit;

  const progress = Math.min(
    100,
    Math.round(((currentTime - startTime) / (endTime - startTime)) * 100)
  );

  return { progress, nextMilestone };
};

